    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $image = Image::where('post_id',$id);
        if ($image != null) {
            $image->delete();
        };

        $comment = Comment::where('post_id',$id);
        if ($comment != null) {
            $comment->delete();
        };

        $post = Post::find($id);
        $post->delete();
        return back();
    }
